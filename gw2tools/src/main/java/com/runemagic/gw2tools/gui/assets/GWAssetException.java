package com.runemagic.gw2tools.gui.assets;

public class GWAssetException extends Exception
{

	public GWAssetException()
	{

	}

	public GWAssetException(String message)
	{
		super(message);
	}

	public GWAssetException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public GWAssetException(Throwable cause)
	{
		super(cause);
	}

}

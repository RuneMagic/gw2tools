package com.runemagic.gw2tools.raid;

public class RaidInterfaceException extends Exception
{

	public RaidInterfaceException()
	{
	}

	public RaidInterfaceException(String message)
	{
		super(message);
	}

	public RaidInterfaceException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public RaidInterfaceException(Throwable cause)
	{
		super(cause);
	}
}

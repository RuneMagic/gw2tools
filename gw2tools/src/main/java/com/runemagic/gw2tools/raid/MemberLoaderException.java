package com.runemagic.gw2tools.raid;

public class MemberLoaderException extends Exception
{

	public MemberLoaderException()
	{
	}

	public MemberLoaderException(String message)
	{
		super(message);
	}

	public MemberLoaderException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public MemberLoaderException(Throwable cause)
	{
		super(cause);
	}
}
